// Викликаємо функцію updateTable() після завантаження сторінки
window.onload = function() {
    updateTable();
};

function calculateAndAddToTable() {
    const date = document.getElementById('dateMond12').value;
    const hours = parseInt(document.getElementById('hoursMond12').value) || 0;
    const tariffPerHour = parseInt(document.getElementById('tariffPerHour').value) || 150;
    const income = calculateIncome(hours, tariffPerHour);
    addToTable(date, hours, income);
    displayResults(); // Оновлення результатів після додавання нового запису
}

function addToTable(date, hours, income) {
    const newRow = createRow(date, hours, income);
    const tableBody = document.getElementById('resultBody');
    tableBody.appendChild(newRow);

    const savedData = JSON.parse(localStorage.getItem('profitData')) || [];
    savedData.push({ date, hours, income });
    localStorage.setItem('profitData', JSON.stringify(savedData)); // Збереження в localStorage
}

function createRow(date, hours, income) {
    const newRow = document.createElement('tr');
    newRow.innerHTML = `
        <td>${date}</td>
        <td>${hours}</td>
        <td>${income}</td>
        <td><button onclick="deleteRow(this)">Видалити</button></td>
    `;
    return newRow;
}

function deleteRow(button) {
    const row = button.parentNode.parentNode;
    const tableBody = document.getElementById('resultBody');
    tableBody.removeChild(row);

    const savedData = JSON.parse(localStorage.getItem('profitData')) || [];
    const index = Array.from(tableBody.children).indexOf(row);
    savedData.splice(index, 1);
    localStorage.setItem('profitData', JSON.stringify(savedData)); // Оновлення localStorage
}

function updateTable() {
    const tableBody = document.getElementById('resultBody');
    const savedData = JSON.parse(localStorage.getItem('profitData')) || [];
    tableBody.innerHTML = ''; // Очистка таблиці перед оновленням

    savedData.forEach(({ date, hours, income }) => {
        const newRow = createRow(date, hours, income);
        tableBody.appendChild(newRow);
    });
}

function calculateIncome(hours, tariffPerHour) {
    return hours * tariffPerHour;
}

// Функція для підрахунку кількості днів
function calculateDays() {
    const tableRows = document.querySelectorAll('#resultTable tbody tr');
    const days = tableRows.length;
    return days;
}

// Функція для підрахунку загальної суми
function calculateTotalIncome() {
    const tableRows = document.querySelectorAll('#resultTable tbody tr');
    let totalIncome = 0;
    tableRows.forEach(row => {
        const incomeCell = row.querySelector('td:nth-child(3)');
        const income = parseFloat(incomeCell.textContent);
        totalIncome += income;
    });
    return totalIncome;
}

// Функція для відображення результатів підрахунку
function displayResults() {
    const days = calculateDays();
    const totalIncome = calculateTotalIncome();
    const resultDiv = document.getElementById('result');
    resultDiv.innerHTML = `Кількість днів: ${days}<br>Загальний прибуток: ${totalIncome}`;
}
function exportToExcel() {
    const table = document.getElementById('resultTable');
    const filename = 'profit_data.xlsx';
    const wsData = [['Дата', 'Години', 'Прибуток']];

    // Отримати дані з таблиці
    const rows = table.querySelectorAll('tbody tr');
    rows.forEach(row => {
        const rowData = [];
        row.querySelectorAll('td').forEach(cell => {
            rowData.push(cell.textContent);
        });
        wsData.push(rowData);
    });

    // Створити новий Workbook та додати аркуш з даними
    const wb = XLSX.utils.book_new();
    const ws = XLSX.utils.aoa_to_sheet(wsData);
    XLSX.utils.book_append_sheet(wb, ws, 'Дані');

    // Зберегти файл
    XLSX.writeFile(wb, filename);
}
function exportToExcel() {
    // Отримайте дані з таблиці
    const table = document.getElementById('resultTable');
    const rows = Array.from(table.getElementsByTagName('tr'));

    // Створіть новий workbook (робочу книгу)
    const workbook = XLSX.utils.book_new();
    const sheetData = [];

    // Додайте заголовки у перший рядок
    const headers = [];
    Array.from(rows[0].getElementsByTagName('th')).forEach(header => {
        if (header.textContent !== "Видалити") { // Пропустіть стовпець з кнопкою "Видалити"
            headers.push(header.textContent);
        }
    });
    sheetData.push(headers);

    // Додайте дані рядків таблиці (без стовпця "Видалити")
    rows.slice(1).forEach(row => {
        const rowData = [];
        Array.from(row.getElementsByTagName('td')).forEach((cell, index) => {
            if (index !== row.cells.length - 1) { // Пропустіть останній стовпець з кнопкою "Видалити"
                rowData.push(cell.textContent);
            }
        });
        sheetData.push(rowData);
    });

    // Додайте рядок з кількістю днів і загальним прибутком
    const days = calculateDays();
    const totalIncome = calculateTotalIncome();
    sheetData.push(['Кількість днів:', days, 'Загальний прибуток:', totalIncome]);

    // Створіть новий worksheet (робочий лист)
    const worksheet = XLSX.utils.aoa_to_sheet(sheetData);

    // Додайте робочий лист у робочу книгу
    XLSX.utils.book_append_sheet(workbook, worksheet, 'Sheet1');

    // Збережіть робочу книгу у форматі Excel
    XLSX.writeFile(workbook, 'result.xlsx');
}


